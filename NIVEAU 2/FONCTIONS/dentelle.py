def printX(n):
    for i in range(n):
        print("X", end="")


def printhashtag(n):
    for i in range(n):
        print("#", end="")


def printi(n):
    for i in range(n):
        print("i", end="")


n = int(input())
printX(n)
print("")
printhashtag(n)
print("")
printi(n)
